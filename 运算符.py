# 算术运算符

# 加法运算符
print(1 + 2);
# 减法运算符
print(1 - 2);
# 乘法运算符
print(1.2 * 2);
# 除法运算符
print(4 / 3); 
# 取余运算符
print(3 % 2);
# 取整除
print(5 // 2);
# 指数  
print(3 ** 5);

# 赋值运算符
num = 1 + 2 - 4;
print(num);
# +=
num1 = 1;
num1 += 2;
print(num1);
# -=
num1 -= 1;
print(num1);
# *=
num1 *= 4;
print(num1);
# /=
num1 /= 2;
print(num1);
# //=
num1 //= 2;
print(num1);
# %=
num2 = 12;
num2 %= 5;
print(num2);
# **=
num1 **= 3;
print(num1);